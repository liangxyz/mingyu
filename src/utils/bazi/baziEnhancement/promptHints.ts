    kongwang: `【空亡详解】空亡只作旁证，先看落在哪一柱、对应哪类人事，再看岁运是否填实、冲实或再次落空。可观察祖上助力、阶段机遇、关系兑现、才华落地、子女互动等环节是否容易延后、落空或有名无实，不可单凭空亡直接定吉凶。`,

    xingchong: `【刑冲合会破】先分原局主线与被引动宫位，再看合冲刑会破是帮助流通还是加重失衡。合多看凝聚牵绊，冲多看变动分离，刑多看压力摩擦，会多看成势放大，破多看折损失守；不可见一项就直接下吉凶结论。`,

    period: `【限运分析】少年(1-16)：学业、性格、健康；中青年(17-45)：事业、婚姻、财富；中老年(46+)：养生、子女、晚年规划。分析各阶段重点任务。`,

    lifespan: `【寿元分析】只允许保守讨论健康承压期、体质短板与养生重点，不得直接推断寿数。纳音、冲克、反吟伏吟、用神受损等只能作为风险排序旁证，必须回到寒暖燥湿、五行偏枯、岁运引动和现实习惯综合判断。`,
