// 3. 五行生克自动生成格局
// ============================================================================

/**
 * 根据天盘与地盘的五行关系自动生成格局
 *
 * @param heavenStem 天盘干
 * @param earthStem  地盘干
 * @returns 自动生成的格局对象
 */
function getAutoPattern(heavenStem: string, earthStem: string): StemPairPattern {
  const heavenElem = stemElements[heavenStem];
  const earthElem = stemElements[earthStem];

  if (!heavenElem || !earthElem) {
    return {
      name: '未知',
      type: 'neutral',
      score: 0,
      summary: `天盘${heavenStem}与地盘${earthStem}的五行属性无法识别。`,
      interpretation: '无法判断两者关系。',
      manifestation: '需结合其他宫位信息综合判断。',
    };
  }

  // 五行相同 → 比和
  if (heavenElem === earthElem) {
    return {
      name: '比和',
      type: 'neutral',
      score: 0,
      summary:
        `天盘${heavenStem}（${heavenElem}）与地盘${earthStem}（${earthElem}）` +
        `五行相同，比和相助。`,
      interpretation:
        '双方五行相同，能量协同稳定，无生克矛盾。' +
        '适合维持现状、稳步推进的事项，不宜冒进或改变。',
      manifestation: '事情进展平稳，内外一致，无明显助力也无明显阻力，按部就班即可。',
    };
  }

  // 天盘克地盘 → 冲（凶）
  if (isControlling(heavenElem, earthElem)) {
    return {
      name: '冲',
      type: 'bad',
      score: -2,
      summary:
        `天盘${heavenStem}（${heavenElem}）克地盘${earthStem}（${earthElem}），` +
        `天盘压制地盘，强行突破之象。`,
      interpretation:
        '天盘主动克制地盘，代表外部力量强行改变内部结构。' +
        '行动力强但带有破坏性，虽然有可能快速见效，但易损伤根基。' +
        '适合需要果断突破的事项，但需注意后续影响。',
      manifestation:
        '做事雷厉风行但容易得罪人；主动出击可能短期内取胜，但长期可能有后遗症；' +
        '宜把握好度，不可过度强势。',
    };
  }

  // 地盘克天盘 → 受阻（凶）
  if (isControlling(earthElem, heavenElem)) {
    return {
      name: '受阻',
      type: 'bad',
      score: -1,
      summary:
        `地盘${earthStem}（${earthElem}）克天盘${heavenStem}（${heavenElem}），` +
        `根基反制行动，阻力来自内部。`,
      interpretation:
        '地盘反克天盘，代表基础或内部因素制约外部行动。' +
        '事情阻力主要来自内部环境、既有制度或下级配合，不易通过外部突破解决。' +
        '宜先巩固内部再图进取，不可硬闯。',
      manifestation:
        '想做却做不了，受到环境、制度或人际关系的限制；' +
        '如审批卡住、预算被砍、下级不配合；宜静不宜动，先理顺内部。',
    };
  }

  // 天盘生地盘 → 泄（凶）
  if (isGenerating(heavenElem, earthElem)) {
    return {
      name: '泄',
      type: 'bad',
      score: -1,
      summary:
        `天盘${heavenStem}（${heavenElem}）生地盘${earthStem}（${earthElem}），` +
        `天盘能量被泄，付出较多。`,
      interpretation:
        '天盘主动生助地盘，代表外部能量流向内部，对天盘而言是消耗。' +
        '付出多于回报，虽然未必全坏，但需要衡量投入产出比。' +
        '适合公益、培养、投资等需要前期投入的事项。',
      manifestation:
        '付出较多而对方受益；短期来看自己吃亏，长期可能积累福报；' +
        '需注意不要过度消耗自己，量力而行。',
    };
  }

  // 地盘生天盘 → 生（吉）
  if (isGenerating(earthElem, heavenElem)) {
    return {
      name: '生',
      type: 'good',
      score: 1,
      summary:
        `地盘${earthStem}（${earthElem}）生天盘${heavenStem}（${heavenElem}），` +
        `根基滋养行动，有后盾之象。`,
      interpretation:
        '地盘生助天盘，代表基础或内部因素滋养外部行动。' +
        '做事有靠山、有资源支持，能够得到他人或环境的帮助。' +
        '适合主动推进、大胆进取的事项。',
      manifestation:
        '有靠山或资源支持，做事顺遂；' + '如上级支持、资金充足、团队配合好；宜乘势进取。',
    };
  }

  // 不应到达此处
  return {
    name: '平',
    type: 'neutral',
    score: 0,
    summary: `天盘${heavenStem}与地盘${earthStem}关系平和。`,
    interpretation: '暂无明确生克判断。',
    manifestation: '需结合其他宫位信息综合判断。',
  };
}

