/**
 * 判断五行相生关系
 * @param source 源五行
 * @param target 目标五行
 * @returns source 是否生 target
 */
export function isGenerating(source: string, target: string): boolean {
  return isSheng(source, target);
}

/**
 * 判断五行相克关系
 * @param source 源五行
 * @param target 目标五行
 * @returns source 是否克 target
 */
export function isControlling(source: string, target: string): boolean {
  return isKe(source, target);
}

/**
 * 获取五行在节令中的旺衰状态
 *
 * 《五行大义》论旺相休囚死：
 *   同令为旺（我当令），令生为相（我生者为相），
 *   生令为休（生我者为休），令克为死（克我者为死），
 *   克令为囚（我克者为囚）。
 *
 * @param element 要判断的元素五行
 * @param seasonElement 当前季节的当令五行
 * @returns '旺' | '相' | '休' | '囚' | '死' | ''
 */
export function getElementStrengthInSeason(
  element: string,
  seasonElement: string,
): '旺' | '相' | '休' | '囚' | '死' | '' {
  if (!element || !seasonElement) return '';
  if (element === seasonElement) return '旺';
  if (isGenerating(seasonElement, element)) return '相';
  if (isGenerating(element, seasonElement)) return '休';
  if (isControlling(seasonElement, element)) return '死';
  if (isControlling(element, seasonElement)) return '囚';
  return '';
}
