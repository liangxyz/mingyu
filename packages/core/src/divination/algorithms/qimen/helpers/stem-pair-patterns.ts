/**
 * 获取天盘干与地盘干的组合格局
 *
 * 在奇门遁甲中，天盘干随九星飞转至某宫，与该宫地盘干形成特定的生克关系。
 * 部分组合有传统的命名格局（如青龙返首、白虎猖狂等），
 * 其余按五行生克关系自动判定。
 *
 * @param heavenStem 天盘干（乙/丙/丁/戊/己/庚/辛/壬/癸）
 * @param earthStem  地盘干（乙/丙/丁/戊/己/庚/辛/壬/癸）
 * @returns 格局对象，若参数不合法则返回 null
 *
 * @example
 * ```ts
 * getStemPairPattern('戊', '丙');
 * // { name: '青龙返首', type: 'good', score: 8, ... }
 *
 * getStemPairPattern('乙', '辛');
 * // { name: '青龙逃走', type: 'bad', score: -8, ... }
 *
 * getStemPairPattern('乙', '乙');
 * // { name: '比和', type: 'neutral', score: 0, ... }
 * ```
 */
export function getStemPairPattern(heavenStem: string, earthStem: string): StemPairPattern | null {
  // 参数校验
  if (!heavenStem || !earthStem) return null;
  if (heavenStem === '甲' || earthStem === '甲') return null;
  if (!stemElements[heavenStem] || !stemElements[earthStem]) return null;

  // 优先查找命名格局
  const key = `${heavenStem}_${earthStem}`;
  const named = NAMED_PATTERNS[key];
  if (named) {
    return { ...named };
  }

  // 无命名格局则按五行生克自动生成
  return getAutoPattern(heavenStem, earthStem);
}

/**
 * 获取所有81种天盘干与地盘干组合的格局列表
 *
 * @returns 全部81种格局对象数组（按天盘干分组排列）
 *
 * @example
 * ```ts
 * const allPairs = listAllStemPairs();
 * console.log(allPairs.length); // 81
 * ```
 */
export function listAllStemPairs(): StemPairPattern[] {
  const results: StemPairPattern[] = [];

  for (const heavenStem of HEAVEN_STEMS) {
    for (const earthStem of EARTH_STEMS) {
      const pattern = getStemPairPattern(heavenStem, earthStem);
      // 内部调用不会返回 null
      if (pattern) {
        results.push(pattern);
      }
    }
  }

  return results;
}
