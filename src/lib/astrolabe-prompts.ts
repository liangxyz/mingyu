function normalizeAstrolabeTopic(topic?: string): AstrolabePromptTopic {
  if (ASTROLABE_PROMPT_TOPICS.includes(topic as AstrolabePromptTopic)) {
    return topic as AstrolabePromptTopic;
  }
  return 'chat';
}

export function getAstrolabePromptConfig(topic?: string) {
  return ASTROLABE_PROMPT_CONFIG[normalizeAstrolabeTopic(topic)];
}

export function getAstrolabeDefaultQuestion(
  topic?: string,
  options: { isCustomQuestion?: boolean } = {},
) {
  if (options.isCustomQuestion) {
    return ASTROLABE_PROMPT_CONFIG.chat.defaultQuestion;
  }

  return getAstrolabePromptConfig(topic).defaultQuestion;
}

export function buildAstrolabeTopicGuidanceSection(topic?: string) {
  return getAstrolabePromptConfig(topic)
    .guidance.map((line) => `- ${line}`)
    .join('\n');
}

export function buildAstrolabeTopicTask(topic?: string) {
  return getAstrolabePromptConfig(topic).task;
}

export function buildAstrolabeTopicOutputRequirement(topic?: string) {
  return getAstrolabePromptConfig(topic).output;
}
